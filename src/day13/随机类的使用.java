package day13;

import java.util.Random;

public class 随机类的使用 {
	//math类使用
	public static void test(String[] args) {
		//Math类的使用
		double a = 2.4;
		int b = (int)a;
		//四舍五入
		long x = Math.round(a);
		
		System.out.println("返回大于等于参数的最小整数："+Math.ceil(a));
		System.out.println("返回小于等于参数的最大整数："+Math.floor(a));
		
		System.out.println(x);
		//[0,1);
		double y = Math.random();
		
	}
	//Random类使用
	public static void main(String[] args) {
		Random r = new Random();
		for(int i = 0;i<3;i++){
			//产生一个小于100的随机正整数[0,100)
			System.out.println(r.nextInt(100));
		}
	}
	
}
