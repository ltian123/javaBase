package day02.运算符;

public class 关系运算符 {
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int c = 10;
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==c);//判断两个变量是否相等
		System.out.println(a!=c);//判断两个变量是否不相等
		
	}
}
